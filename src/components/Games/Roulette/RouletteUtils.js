function fromNumberToColor(number) {
  if (number === 0) {
    return "green";
  } else if (red.indexOf(number) !== -1) {
    return "red";
  } else {
    return "black";
  }
}

function fromChipValueToColor(number) {
  switch (number) {
    case 100:
      return "purple";
    case 500:
      return "green";
    case 2500:
      return "pink";
    case 10000:
      return "blue";
    case 50000:
      return "black";
    default:
      return "";
  }
}

function checkPairs(bet, bets) {
  let totalAmt = 0

  correlatedBets[bet].forEach((num) => {
    // iterate through each possible outcome for any spot that has chips placed
    let highestWinning = 0
    num.forEach((spot) => {
      if (bets[spot] !== undefined) {
        highestWinning += (bets[spot].mult * bets[spot].total)
      }
    })

    if (highestWinning > totalAmt) {
      totalAmt = highestWinning
    }
  })

  return totalAmt
}

function getMultiplier(spot) {
  // 4 connected numbers
  if (spot.length === 4) {
    return 8
    // 3 connected numbers
  } else if (spot.length === 3) {
    return 12
    // 2 connected numbers
  } else if (spot.length === 2) {
    return 18
  } else if (spot.length === 1) {
    if (spot[0] === "red" || spot[0] === "black" || spot[0] === "even" || spot[0] === "odd" || spot[0] === "first18" || spot[0] === "second18") {
      return 1
    } else if (spot[0] === "1st" || spot[0] === "2nd" || spot[0] === "3rd" || spot[0] === "first12" || spot[0] === "second12" || spot[0] === "third12") {
      return 2
    } else {
      // exact number
      return 35
    }
  }
}

function getSpotNumber(spot) {
  switch (spot) {
    case "red":
      return 0
    case "black":
      return 1
    case "even":
      return 0
    case "odd":
      return 1
    case "first18":
      return 10
    case "second18":
      return 28
    case "1st":
      return 1
    case "2nd":
      return 2
    case "3rd":
      return 3
    case "first12":
      return 6
    case "second12":
      return 18
    case "third12":
      return 30
    default:
      return Number(spot)
  }
}

function getSubgame(spot) {
  if (spot === "red" || spot === "black") {
    return 0
  } else if (spot === "even" || spot === "odd") {
    return 1
  } else if (spot === "first18" || spot === "second18") {
    return 2
  } else if (spot === "1st" || spot === "2nd" || spot === "3rd") {
    return 3
  } else if (spot === "first12" || spot === "second12" || spot === "third12") {
    return 4
  } else {
    return 5
  }
}

